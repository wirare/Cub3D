void init_player_timing(t_cub3d *cub3d)
{
	cub3d->player.pos = (t_vector){22, 12};
	cub3d->player.dir = (t_vector){-1, 0};
	cub3d->player.plane = (t_vector){0, 0.66};
	cub3d->timing = (t_timing){0, 0, 0};
}

